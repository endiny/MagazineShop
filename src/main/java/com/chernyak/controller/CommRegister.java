package com.chernyak.controller;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;


public class CommRegister extends Command {
	
    /**
     * Page with the results of registration
     * @param request
     * @param response
     * @throws ServletException
     * @throws IOException
     */
    @Override
	public void execute(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		String firstName = request.getParameter("firstName");
		String lastName = request.getParameter("lastName");
		Integer courseId = Integer.valueOf(request.getParameter("course"));
		Boolean result = clientService.registerStudent(firstName, lastName, courseId);
		String massage = null ;
		if (result) {
			massage = "???? ?????????????? ???????????????????????????????????? ???? ????????." ;
		} else {
			massage = "?????? ???? ?????????? ???? ??????. ???????????????????? ?????????????????? ??????????????????????.";
		}
		request.setAttribute("massege", massage);
		request.getRequestDispatcher("/WEB-INF/end.jsp").forward(request, response);
	}

}
